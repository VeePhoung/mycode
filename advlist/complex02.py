#!/usr/bin/env python3

def main():
    """Create and display a list of animals with three-letter names."""
    
    # List of animals with three-letter names
    animals = ["Cat", "Dog", "Bat", "Ant", "Owl"]

    # Print each animal in the list
    for animal in animals:
        print(animal)

# Call the main function
if __name__ == "__main__":
    main()
